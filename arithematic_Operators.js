// Arithematic Operators are +, -, *, /, %, **

var a = 5,
  b = 2;
console.log(`${a} + ${b} = ${a + b}`);
console.log(`${a} - ${b} = ${a - b}`);
console.log(`${a} * ${b} = ${a * b}`);
console.log(`${a} / ${b} = ${a / b}`); // gives Quotient values
console.log(`${a} % ${b} = ${a % b}`); // gives Remainder value

var base = 3,
  exponent = 4;
console.log(
  `${base} is raised to the power of ${exponent} :${base ** exponent}`
);
